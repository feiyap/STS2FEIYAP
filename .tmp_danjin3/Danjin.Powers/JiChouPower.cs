public sealed class JiChouPower : DanjinPower
{
	public override PowerType Type => (PowerType)2;

	public override PowerStackType StackType => (PowerStackType)1;

	public override async Task AfterSideTurnEnd(PlayerChoiceContext choiceContext, CombatSide side, IEnumerable<Creature> participants)
	{
		//IL_001e: Unknown result type (might be due to invalid IL or missing references)
		//IL_001f: Unknown result type (might be due to invalid IL or missing references)
		if (side != ((PowerModel)this).Owner.Side && ((PowerModel)this).Amount > 0)
		{
			await PowerCmd.Apply<JiChouPower>(choiceContext, ((PowerModel)this).Owner, -1m, ((PowerModel)this).Owner, (CardModel)null, true);
		}
	}
}
